        $(document).ready(function() {
            /=================================================  VIDEO PLAY BUTTON STARTS ====================================================/
            var windowWidth = $(window).width();
            if (windowWidth > 640) {
                $(".Video-playbtn").click(function() {
                    var iframe = $(".StoryVideo iframe");
                    var src = iframe.attr("src");
                    if (src.indexOf("autoplay=1") === -1) {
                        iframe.attr("src", src + (src.indexOf("?") === -1 ? "?" : "&") + "autoplay=1");
                    }
                    $(this).hide();
                    $(this).closest(".HomeVideoInner").find(".HomeVideoTitleSection").addClass("animate_it");
                    $(this).closest(".HomeVideoInner").find(".HomeVideoTitleSection").removeClass("animate_reverse");
                    return false;
                });

                document.addEventListener('play', function(e) {
                    if (e.target.nodeName === 'IFRAME') {
                        $('.Video-playbtn').hide();
                        $(e.target).closest(".HomeVideoInner").find(".HomeVideoTitleSection").addClass("animate_it");
                        $(e.target).closest(".HomeVideoInner").find(".HomeVideoTitleSection").removeClass("animate_reverse");
                    }
                }, true);

                document.addEventListener('pause', function(e) {
                    if (e.target.nodeName === 'IFRAME') {
                        $('.Video-playbtn').show();
                        $(e.target).closest(".HomeVideoInner").find(".HomeVideoTitleSection").removeClass("animate_it");
                        $(e.target).closest(".HomeVideoInner").find(".HomeVideoTitleSection").addClass("animate_reverse");
                    }
                }, true);

                $(function() {
                    $('iframe').on('ended', function() {
                        $(this).closest(".HomeVideoInner").find(".HomeVideoTitleSection").removeClass("animate_it");
                        $(this).closest(".HomeVideoInner").find(".HomeVideoTitleSection").addClass("animate_reverse");
                        $(this).closest(".HomeVideoInner").find(".Video-playbtn").show();
                    });
                });

            }
        });
